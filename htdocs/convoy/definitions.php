<br>
CY-004A-01a - Scania box truck with white cab, white container, black
trailer with <font color="#ff6600">"Ansett" labels to the front</font>&nbsp;
<i><font color="#3366ff">(a documented variation)</font></i><br>
<br>
CY-004A-02a - Scania box truck with white cab, white
container, black trailer with <font color="#ff6600">"Ansett" labels to
the rear</font>&nbsp; <font color="#3366ff"><i>(another documented
variation)</i><br>
</font><br>
&nbsp;&nbsp;&nbsp;&nbsp;<font color="#3333ff"> If we found and verified
the same model with one label to the front and the other label to the
rear it&nbsp; would be numbered as: </font><br>
<br>
CY-004-02b - Scania box truck with white cab, white
container, black trailer with <font color="#ff6600">one "Ansett"
labels to the front and the other label to the rear.</font>&nbsp; <font
 color="#3333ff">(a previously unreported variation)</font><br>
&nbsp;&nbsp;&nbsp; <br>
&nbsp;&nbsp;&nbsp;&nbsp;<font color="#3333ff"> The variations that sets
these model apart from the other two models would be listed in a
contrasting color for easy identification.</font><br>
