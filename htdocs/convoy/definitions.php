<br>
</font>Proposed Numbering
Standards</font><br>
<br>
</font>LLL- ###L - ##L<br>
<br>
Two or Three Letters representing the model series<br>
Three Numbers followed by a Letter representing the Matchbox model
number<br>
Two numbers followed by a Letter representing the different variations
of that model<br>
<br>
