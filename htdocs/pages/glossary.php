<h2 id="b">B</h2>

<dl>
<dt>Blister</dt>
<dd>Common term for the plastic packaging that a model comes in</dd>
</dl>

<h2 id="c">C</h2>

<dl>
<dt>C5 through C10</dt>
<dd>Condition codes for stating the condition of a model, as follows:
<ul>
<li>C5 - Very Poor - wear evident overall
<li>C6 - Good - wear in spots noticeable
<li>C7 - Fine - minor wear - scratches noticeable - otherwise clean
<li>C8 - Excellent - very slight wear - very clean with gloss
<li>C9 - Near Mint - flaws hardly noticeable
<li>C10 - Mint - pristine - like new
</ul></dd>
<dt>Case</dt>
<dd>An entire box of models as packed by the manufacturer</dd>
<dt>Chase Car</dt>
<dd>A vehicle produced in limited quantaties and therefore is harder to find.  Possibly the best known chase vehicles are the Treasure Hunts that Mattel Inc. include in the Hot Wheels range each year, these car are packed at random in one to every 4 or 5 cartons.</dd>
<dt>Code 1</dt>
<dd>Any model produced by the original manufacturer</dd>
<dt>Code 2</dt>
<dd>Any model produced by the original manufacturer but contains authorised after market modifications such as additional tampo printing</dd>
<dt>Code 3</dt>
<dd>Any model that has after market modifications that have not been approved by the original manufacturer</dd>
<dt>Code 4</dt>
<dd>Matchbox limited quantity models (one-offs) not put on general sale. Normally company issued gifts to
individuals or organization.</dt>

</dl>

<h2 id="d">D</h2>

<dl>
<dt>Decal</dt>
<dd>A label or design that is prepared on a paper or plastic backing before being transferred to the model</dd>
<dt>Die</dt>
<dd>The metallic mould used for producing the models</dd>
<dt>Diecast</dt>
<dd>The method of mass-producing, under great pressure, molten zinc and white-metal alloys into permanent moulds.</dd>
<dt>Dinky</dt>
<dd>A brand of diecast originally made by Meccano.  Matchbox acquired the brand in the last 1980s.
</dl>

