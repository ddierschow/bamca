<h2 id="r">R</h2>

<dl>
<dt>R/C</dt>
<dd>An abbreviation for Remote Control</dd>
<dt>Regular Wheels</dt>
<dd>The term used to described any Matchbox prior to the introduction of the &#8220;Superfast&#8221; wheels in 1969/1970</dd>
<dt>ROW</dt>
<dd>Abbreviation for the Rest of the World Market (ie: Everywhere but the USA)</dd>
<dt>RWR</dt>
<dd>A common abbreviation for &#8220;Real Working Rigs&#8221;</dd>
</dl>

<h2 id="s">S</h2>

<dl>
<dt>Scale</dt>
<dd>Indication of the relative size of a diecast vehicle. Sometimes indicated with a slash, as in &#8220;1/18&#8243; scale or a colon, as in &#8220;1:18&#8243; For a better idea of individual scales, refer to <a title="Model scales" href="http://cobratoys.com.au/blog/model-scales/">this blog post</a></dd>
<dt>Short Card</dt>
<dd>Descriptive term for the shorter cards used in the ROW markets</dd>
<dt>Superfast</dt>
<dd>Subset of Matchbox used at various times in the history of the brand beginning with the introduction of the Superfast wheels in 1969/1970</dd>
<dt>Superfast Wheels</dt>
<dd>A new style of faster rolling wheels introduced in 1969/1970 to counteract the introduction of Hot Wheels cars</dd>
</dl>

<h2 id="t">T</h2>

<dl>
<dt>Tampo</dt>
<dd>More commonly known as pad printing, this is the means by which the manufacturer paints the designs and details like headlights and logo&#8217;s onto a model.</dd>
<dt>Tyco Toys</dt>
<dd>Owner of the Matchbox brand from 1993 till its take-over by Mattel, Inc in 1997</dd>
</dl>

