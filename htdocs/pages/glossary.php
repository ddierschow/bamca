<h2 id="l">L</h2>

<dl>
<dt>Label</dt>
<dd>A design or logo on paper backing applied to the model</dd>
<dt>LAAM</dt>
<dd>Abbreviation for the Latin American Market</dd>
<dt>Lesney</dt>
