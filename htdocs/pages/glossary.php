</dl>

<h2 id="m">M</h2>

<dl>
<dt>MAN</dt>
<dd>This is an abbreviation for MANufaturing number, the number used by the manufacturer to identify the model components, it has been used by all owners of the Matchbox brand since the early 1980&#8242;s</dd>
<dt>Mack Code</dt>
<dd>Developed by Charlie Mack (A renowned USA based collector) as a way of identifying Matchbox models. These codes are based upon the release number of the model followed by an incremental letter.</dd>
<dt>Mattel Inc.</dt>
<dd>Owners of the Matchbox brand from 1997 when they took over Tyco Toys</dd>
<dt>MB</dt>
<dd>A common abbreviation for &#8220;Matchbox&#8221;, also used as the leading letters in the MAN coding system.</dd>
<dt>MIB</dt>
<dd>A common abbreviation for &#8220;Mint In Box&#8221; or &#8220;Mint In Blister&#8221;</dd>
<dt>MOMC</dt>
<dd>A common abbreviation for &#8220;Mint On Mint Card&#8221;</dd>
<dt>MOY</dt>
<dd>A common abbreviation for &#8220;Models of Yesteryear&#8221;</dd>
</dl>

